  def card_get
    if deck_quan_cards == 0
      puts 'колода кончилась'
      raise 'Колода кончилась'
    else
      # puts deck_quan_cards
      number = rand(1 .. 52)
      if @deck[number] != nil
        card = @deck[number]
        @deck[number] = nil
      else
        while  @deck[number] == nil
          number = rand(1 .. 52)
        end
        card = @deck[number]
        @deck[number] = nil
      end
      card
    end
  end

  def score_get(card)
